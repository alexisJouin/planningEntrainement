$(document).ready(function (e) {

    $("body").find('script').remove();
    $("center").remove();
    $("input").unbind( "click" );
    $("button").unbind( "click" );
 
});

$(document).load(function (e) {

    $("body").find('script').remove();
    $("center").remove();
    $("input").unbind( "click" );
    $("button").unbind( "click" );
});

